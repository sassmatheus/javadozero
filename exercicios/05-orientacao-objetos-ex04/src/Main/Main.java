package Main;

import java.util.Locale;
import java.util.Scanner;

import CurrencyConverter.CurrencyConverter;

public class Main {

	public static void main(String[] args) {
		//Faça um programa para ler a cotação do dólar, e depois um valor em dólares a ser comprado por 
		//uma pessoa em reais. Informar quantos reais a pessoa vai pagar pelos dólares, considerando ainda 
		//que a pessoa terá que pagar 6% de IOF sobre o valor em dólar. Criar uma classe CurrencyConverter
		//para ser responsável pelos cálculos.
		Locale.setDefault(Locale.US);
		Scanner scan = new Scanner(System.in);
		
		CurrencyConverter converter = new CurrencyConverter();
		
		System.out.println("Qual o preço do dólar?");
		converter.dolar = scan.nextDouble();
		System.out.println("Quantos dólares deseja comprar?");
		converter.reais = scan.nextDouble();
		
		System.out.printf("O valor pago em reais será: R$ %.2f", converter.conversor());
		
		scan.close();
	}

}
